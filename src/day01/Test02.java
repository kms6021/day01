package day01;

public class Test02 {
public static void main(String[] args) {
System.out.println("이름 : 김민수");
System.out.println("나이 : 20살");
System.out.println("주소 : 고양시");

	// \n : 엔터와 비슷, \t : 탭크기만큼 왼쪽으로 이동
	// \" : "(큰따옴표 표현), \\ : 역슬러시 하나 표현
		System.out.println("안\n녕하세요");
		System.out.println("1234567\t12\t12");
		// \t = 스페이스바 8칸 / 8칸이 모두 찼다면 \t 하면 8칸이 새로 생김
		System.out.println("\"안녕하세요\"");
		System.out.println("안\\녕");
	}
}
